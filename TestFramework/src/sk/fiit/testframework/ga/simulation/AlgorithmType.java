package sk.fiit.testframework.ga.simulation;

public class AlgorithmType {
	
	private boolean geneticAlgorithm;
	
	private boolean geneticPrograming;

	public boolean isGeneticAlgorithm() {
		return geneticAlgorithm;
	}

	public void setGeneticAlgorithm(boolean geneticAlgorithm) {
		this.geneticAlgorithm = geneticAlgorithm;
	}

	public boolean isGeneticPrograming() {
		return geneticPrograming;
	}

	public void setGeneticPrograming(boolean geneticPrograming) {
		this.geneticPrograming = geneticPrograming;
	}
	
	
}
